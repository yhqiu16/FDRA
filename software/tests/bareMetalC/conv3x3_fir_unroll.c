/* Array initialization. */
void init_array()
{
    int i,j;
    for (i=0;i<SIZE; i++){
      A[i] = i;
    }
    for (i = 0; i < NTAPS; i++)
      {
        input[i] = i;
        output[i] = 0;
        res[i] = 0;
      }
}

__attribute__((noinline))
void convolution2d() {
	for (int i = 1; i < NI -1; i++) {
		for (int j = 1; j < NJ-1; j++) {
#ifdef CGRA_COMPILER
please_map_me();
#endif
	/*For 3x3 filter*/
		C[i*NJ + j] = c11 * A[(i - 1)*NJ + (j - 1)]  +  c12 * A[(i + 0)*NJ + (j - 1)]  +  c13 * A[(i + 1)*NJ + (j - 1)]
				+ c21 * A[(i - 1)*NJ + (j + 0)]  +  c22 * A[(i + 0)*NJ + (j + 0)]  +  c23 * A[(i + 1)*NJ + (j + 0)]
				+ c31 * A[(i - 1)*NJ + (j + 1)]  +  c32 * A[(i + 0)*NJ + (j + 1)]  +  c33 * A[(i + 1)*NJ + (j + 1)];

	// /*For 2x2 filter*/
	// 	B[i*NJ + j] = c11 * A[(i - 1)*NJ + (j - 1)]  +  c12 * A[(i + 0)*NJ + (j - 1)] 
	// 			+ c21 * A[(i - 1)*NJ + (j + 0)]  +  c22 * A[(i + 0)*NJ + (j + 0)];
		}
	}
}

__attribute__((noinline))
void fir()//(int input[], int output[], int coefficients[])
/*   input :           input sample array */
/*   output:           output sample array */
/*   coefficient:      coefficient array */
{
  int i;
  int j = 0;

 for(j=0; j< NTAPS; ++j) {
 	  int sum = 0;
//    #pragma unroll 4
    for (i = 0; i < NTAPS; ++i) {
      #ifdef CGRA_COMPILER
      please_map_me();
      #endif
      sum += input[i] * coefficients[i];
    }
    res[j] += sum;
 }
}


void result_check()
{
  int i, j;

  for (i = 0; i < SIZE; i++)
  {
    if (C[i] != B[i])
        printf("There is an error in location (%d)[%d, %d]\n", i, C[i], B[i]);
  }
  for (i = 0; i < NTAPS; i++)
  {
    if (res[i] != output[i]) printf("There is an error in location (%d)[%d, %d]\n", i, res[i], output[i]);
  }
}


int main(){
    long long unsigned start;
    long long unsigned end;
    init_array();
    printf("Initialization finished!\n");

    start = rdcycle();
    /* Run kernel. */
    convolution2d();
    fir();
    end = rdcycle();
    printf("It takes %d cycles for CPU to finish the task.\n", end - start);

    void* cgra_din_addr[18] = {A, A, A, A, A, A, A, A, A, coefficients, coefficients, coefficients, coefficients, input, input, input, input, output};
    void* cgra_dout_addr[2] = {(void*)B+128, output};
    start = rdcycle();
    cgra_execute(cgra_din_addr, cgra_dout_addr);
    volatile int result = fence(1);
    end = rdcycle();
    printf("It takes %d cycles for CGRA to finish the task(%d).\n", end - start, result);

    result_check();
    printf("Done!\n");

    return 0;
}
